/* Реализация функции strcmp на Си включает посимвольное
 * сравнение двух строк до первого различия или до конца
 * одной из строк, возвращая отрицательное число, ноль или
 * положительное число, в зависимости от того, какая строка
 * меньше, равна или больше. Обычно это делается в цикле while,
 * сравнивая символы по указателям и двигая указатели,
 * пока не встретится '\0' или не найдено различие;
 * если все символы совпали до конца, строки равны */

#include <stdio.h> // Для printf
#include <stddef.h> // Для size_t

int my_strcmp(const char *str1, const char *str2) {
    // Пока оба указателя не достигли конца строки и символы равны
    while (*str1 != '\0' && *str2 != '\0' && *str1 == *str2) {
        str1++; // Переходим к следующему символу в первой строке
        str2++; // Переходим к следующему символу во второй строке
    }

    // Если одна строка закончилась раньше другой (один из них '\0'),
    // то возвращаем разницу их значений.
    // Например, если *str1 = 'a' и *str2 = '\0', вернется 'a' (положительное).
    // Если *str1 = '\0' и *str2 = 'a', вернется -'a' (отрицательное).
    // Если обе строки закончились одновременно (str1 == str2), вернется 0.
    return *str1 - *str2;
}

int main() {
    char *s1 = "hello";
    char *s2 = "hello";
    char *s3 = "hell";
    char *s4 = "world";
    char *s5 = "hella";

    printf("'%s' vs '%s': %d\n", s1, s2, my_strcmp(s1, s2)); // Ожидаем 0 (равны)
    printf("'%s' vs '%s': %d\n", s1, s3, my_strcmp(s1, s3)); // Ожидаем > 0 (s1 длиннее)
    printf("'%s' vs '%s': %d\n", s3, s1, my_strcmp(s3, s1)); // Ожидаем < 0 (s3 короче)
    printf("'%s' vs '%s': %d\n", s1, s4, my_strcmp(s1, s4)); // Ожидаем < 0 ('h' < 'w')
    printf("'%s' vs '%s': %d\n", s1, s5, my_strcmp(s1, s5)); // Ожидаем > 0 ('o' > 'a')

    return 0;
}
